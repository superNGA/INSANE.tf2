#include "BaseDrawObj.h"


///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////
void IDrawObj_t::SetDrawInLobby(bool bDrawInLobby)
{
    m_bDrawInLobby = bDrawInLobby;
}


///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////
void IDrawObj_t::SetDrawInGame(bool bDrawInGame)
{
    m_bDrawInGame = bDrawInGame;
}


///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////
void IDrawObj_t::SetVisible(bool bVisible)
{
    SetDrawInGame(bVisible);
    SetDrawInLobby(bVisible);
}


///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////
bool IDrawObj_t::Is3D() const
{
    return m_bIs3D;
}


///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////
bool IDrawObj_t::ShouldDrawInLobby() const
{
    return m_bIs3D == false && m_bDrawInLobby == true;
}


///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////
bool IDrawObj_t::ShouldDrawInGame() const
{
    return m_bDrawInGame;
}
