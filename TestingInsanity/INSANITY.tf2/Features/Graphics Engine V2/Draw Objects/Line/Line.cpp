#include "Line.h"


///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////
unsigned int Line_t::GetVertexCount() const
{
    return 2;
}


///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////
void Line_t::SetBlur(const int iBlur)
{
    m_vertex[0].m_flBlurAmmount = static_cast<float>(iBlur);
    m_vertex[1].m_flBlurAmmount = static_cast<float>(iBlur);
}


///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////
void Line_t::SetAs2DObject(bool bIs2D)
{
    m_vertex[0].m_flStrictly2D = (bIs2D == true ? 1.0f : 0.0f);
    m_vertex[1].m_flStrictly2D = (bIs2D == true ? 1.0f : 0.0f);
}


///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////
const Vertex* Line_t::GetVertexData() const
{
    return m_vertex;
}


///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////
void Line_t::SetColor(unsigned char r, unsigned char g, unsigned char b, unsigned char a)
{
    m_vertex[0].m_clr.Set(r, g, b, a);
    m_vertex[1].m_clr.Set(r, g, b, a);
}


///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////
void Line_t::InvertColors()
{
    m_vertex[0].m_flInvertColors = 1.0f;
    m_vertex[1].m_flInvertColors = 1.0f;
}


///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////
void Line_t::SetPoints(const vec& vMin, const vec& vMax)
{
    m_vertex[0].m_vPos = vMin;
    m_vertex[1].m_vPos = vMax;
}
